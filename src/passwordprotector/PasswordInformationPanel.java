    public int getProblems () {
        String psw1 = this.getPsw1();
        String psw2 = this.getPsw2();
        
        // psw1 == null || psw2 == null;
        if (psw1.equals("[]") || psw2.equals("[]")) {
            psw1 = null;
            psw2 = null;
            return 1;
        }
        // psw1 != psw2
        else if (!psw1.equals(psw2)) {
            psw1 = null;
            psw2 = null;
            return 2;
        }
        else if (jTextField2.getText().equals("") && jTextField3.getText().equals("")) {
            return 3;
        }
        else {
            return 0;
        }
    }
    
